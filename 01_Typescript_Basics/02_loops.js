// In this file, you will learn how to repeat actions using loops,
// and how to make decisions using conditions.
// A loop repeats something again and again.
// Example: Count from 1 to 5
// 'for' means: repeat this block of code.
// 'i' is a counter that starts at 1 and goes up to 5.
for (var i = 1; i <= 5; i++) {
    console.log("Number:", i);
}
// A condition checks if something is true or false.
// If it's true, do one thing. If not, do something else.
var points = 8;
// This checks if 'points' is 10 or more.
if (points >= 10) {
    console.log("Great! You have enough points.");
}
else {
    console.log("Sorry, not enough points.");
}
